/* ── Helper: get all cities for a given județ ── */
export function getCitiesForJudet(judet: string): string[] {
    return ROMANIAN_JUDETE[judet] ?? [];
}

/* ── Helper: get all județe names (sorted) ── */
export function getJudeteNames(): string[] {
    return Object.keys(ROMANIAN_JUDETE).sort((a, b) => a.localeCompare(b, 'ro'));
}

/* ── Helper: find country by code ── */
export function getCountryByCode(code: string): Country | undefined {
    return COUNTRIES.find(c => c.code === code);
}
